export function getCartTranslation(locale: 'en' | 'nl' | 'pl', key: keyof CartTranslations['en']): string {
  const translations = cartTranslations[locale];
  return translations[key] || cartTranslations.en[key];
}

export function formatCartMessage(message: string, amount?: number): string {
  if (amount !== undefined) {
    return message.replace('{amount}', amount.toFixed(2));
  }
  return message;
}
